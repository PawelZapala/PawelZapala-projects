package JavaLoops;

public class LoopDemo {
    public static void main(String[] args) {
        int a = 0;
        while (a <=5 ) {
            System.out.println(a);
            a++;
        }

        int j = 0;
        do {
            System.out.println(j);
            j++;
        }    while (j <= 5) ;
        }
}
